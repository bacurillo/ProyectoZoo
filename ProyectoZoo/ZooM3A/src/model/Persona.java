package model;

import java.util.Date;

/**
 *
 * @author ALEJO
 */
public class Persona {

    private String cedula;
    private String nombre;
    private String apellido;
    private String correo;
    private String telefono;
    private Date fechaRegistro;
    private boolean estadoPer;

    public Persona() {
    }

    public Persona(String cedula, String nombre, String apellido, String correo, String telefono, Date fechaRegistro, boolean estadoPer) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.apellido = apellido;
        this.correo = correo;
        this.telefono = telefono;
        this.fechaRegistro = fechaRegistro;
        this.estadoPer = estadoPer;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public boolean isEstadoPer() {
        return estadoPer;
    }

    public void setEstadoPer(boolean estadoPer) {
        this.estadoPer = estadoPer;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public Date getFechaRegistro() {
        return fechaRegistro;
    }

    public void setFechaRegistro(Date fechaRegistro) {
        this.fechaRegistro = fechaRegistro;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

}
